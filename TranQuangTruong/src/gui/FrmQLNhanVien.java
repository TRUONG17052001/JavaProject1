    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        Date ngay = null;
        try {
            ngay = df.parse(txtNgay.getText());
        } catch (ParseException ex) {
            Logger.getLogger(FrmQLNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
        Duan da = (Duan) cboDuan.getSelectedItem();
        Nhanvien nv = new Nhanvien(txtMa.getText(), da, txtTen.getText(), ngay);
        snv.saveOrUp(nv);
        JOptionPane.showMessageDialog(null, "Thêm thành công!");
        loadData();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        String ma= txtMa.getText();
        Nhanvien nv= snv.findById(ma);
        nv.setTennv(txtTen.getText());
        try {
            nv.setNgaysinh(df.parse(txtNgay.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(FrmQLNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
        nv.setDuan((Duan) cboDuan.getSelectedItem());
        snv.saveOrUp(nv);
        JOptionPane.showMessageDialog(null, "Cập nhật thành công!");
        loadData();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        txtMa.enable(false);
        txtMa.setText(jTable1.getValueAt(row, 0) + "");
        txtTen.setText(jTable1.getValueAt(row, 1) + "");
        txtNgay.setText(jTable1.getValueAt(row, 2) + "");
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        String ma= txtMa.getText();
        Nhanvien nv= snv.findById(ma);
        snv.delete(nv);
        JOptionPane.showMessageDialog(null, "Xóa thành công!");
        loadData();
    }//GEN-LAST:event_btnXoaActionPerformed

