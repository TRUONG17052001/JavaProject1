    public void loadData() {
        model.setRowCount(0);
        for (Nhanvien nv : snv.findAll()) {
            Object[] row = {nv.getManv(), nv.getTennv(), df.format(nv.getNgaysinh()), nv.getDuan()};
            model.addRow(row);
        }
    }

